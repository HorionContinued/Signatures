<script lang="ts">
	import Fuse from 'fuse.js';
	import VirtualList from '@sveltejs/svelte-virtual-list';
	import { faGithub, faDiscord } from '@fortawesome/free-brands-svg-icons'
	import Fa from 'svelte-fa'
    import Modal, { bind } from 'svelte-simple-modal';
	import { fly, slide } from 'svelte/transition';
	import { onMount } from 'svelte';
	import { modalStore } from '../stores';

	import Entry from '../components/Entry.svelte';
	import EntryFull from '../components/EntryFull.svelte';
	import type EntryData from '../model/EntryData.svelte'; 

	let all: EntryData[];

	let searchTerm = '';
	let fuse: Fuse<EntryData>;
	$: if(all) fuse = new Fuse(all, {
			keys: ['name', 'description'],
			threshold: 0.25,
			includeMatches: true,
			useExtendedSearch: true
	});
	

	let searchResults: {item: EntryData, matches?: ReadonlyArray<Fuse.FuseResultMatch>}[];
	$: if(fuse) searchResults = (searchTerm.length > 0) ? fuse.search(searchTerm) : all.map(v => ({item: v, matches: []}));
	
	let showNotice = true;

	onMount(async () => {
		let entrydata = await import("../model/EntryData.svelte");
		// @ts-expect-error stupid typescript
		all = await entrydata.get();

		if (window.location.hash.length > 0) {
			const entry = all.find(e => e.cleanName === window.location.hash.slice(1));
			if (entry) {
				// @ts-expect-error cba to type this
				modalStore.set(bind(EntryFull, { entry }));
				console.log(entry);
			}
		}
	});
</script>

